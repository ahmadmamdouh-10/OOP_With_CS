using System;
using System.Collections.Generic;
using System.Text;

namespace Day3
{
    class _2Dpoint
    {
        private int x, y;

        public _2Dpoint(int _x, int _y)
        {
            x = _x;
            y = _y;
        }

        public int GetX()
        {
            return x;
        }

        public int GetY()
        {
            return y;
        }

    }
}
